package com.cinthyasophia.tema11.Ejercicio07;

public class Asiento {
    private int numero;
    private char fila;

    public Asiento(int numero, char fila){
        this.numero= numero;
        this.fila= fila;
    }

    public int getNumero() {
        return numero;
    }

    public char getFila() {
        return fila;
    }
}
